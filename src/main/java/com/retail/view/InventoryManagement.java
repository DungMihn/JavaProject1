    private void deleteInventoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteInventoryBtnActionPerformed
        int row = inventoryTable.getSelectedRow();
        if (row == -1){
            JOptionPane.showMessageDialog(this, "Vui lòng chọn inventory trước", "Loi", JOptionPane.ERROR_MESSAGE);
        }else{
            int confirm = JOptionPane.showConfirmDialog(this, "Bạn chắc chắn muốn xóa không");
            
            if(confirm == JOptionPane.YES_OPTION){
                int inventoryId = Integer.parseInt(String.valueOf(inventoryTable.getValueAt(row,0)));
                inventoryController.deleteInventory(inventoryId);
                loadInventoryData();
            }
            
        }
    }//GEN-LAST:event_deleteInventoryBtnActionPerformed

