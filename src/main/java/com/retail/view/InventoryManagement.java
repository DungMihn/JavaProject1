    private void addInventoryBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addInventoryBtnActionPerformed
        int productId = Integer.parseInt(productIdTextField.getText());
        int stockQuantity = Integer.parseInt(stockQuanlityTextField.getText());

        boolean success = inventoryController.addInventory(productId, stockQuantity);
    
    if (success) {
        JOptionPane.showMessageDialog(this, "Added inventory successfully!");
        loadInventoryData(); // Làm mới bảng sau khi thêm
    } else {
        JOptionPane.showMessageDialog(this, "Failed to add inventory!");
    }
    }//GEN-LAST:event_addInventoryBtnActionPerformed

    private void loadInventoryData() {
    DefaultTableModel model = (DefaultTableModel) inventoryTable.getModel();
    model.setRowCount(0); // Xóa dữ liệu cũ

    List<Inventory> inventories = inventoryController.getAllInventories();
    for (Inventory inventory : inventories) {
        model.addRow(new Object[]{
            inventory.getInventoryId(),
            inventory.getProductId(),
            inventory.getStockQuantity(),
            inventory.getLastUpdated()
        });
    }
}

