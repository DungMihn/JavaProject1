    private void addLabelListeners() {
        // Thêm MouseListener cho từng JLabel
        addStockEntryLabel.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                cardLayout.show(contentPanel, "StockEntry"); // Hiển thị panel StockEntry
                stockEntryPanel.initializeData(); // Gọi lại phương thức initializeData()
            }
        });

        stockEntryListLabel.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                cardLayout.show(contentPanel, "StockEntryList"); // Hiển thị panel Function1
                stockEntryListPanel.loadStockEntryData();
            }
        });

        InventoryLabel.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                cardLayout.show(contentPanel, "Inventory"); // Hiển thị panel Function2
            }
        });


    }

    private void addLabelHoverEffects() {
        // Hiệu ứng cho addStockEntryLabel
        addHoverEffect(addStockEntryLabel);

        // Hiệu ứng cho stockEntryListLabel
        addHoverEffect(stockEntryListLabel);

        // Hiệu ứng cho InventoryLabel
        addHoverEffect(InventoryLabel);


    }

    private void addHoverEffect(JLabel label) {
        label.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseEntered(java.awt.event.MouseEvent evt) {
                label.setForeground(Color.BLUE); // Đổi màu chữ khi di chuột qua
            }

            @Override
            public void mouseExited(java.awt.event.MouseEvent evt) {
                label.setForeground(Color.BLACK); // Trả lại màu chữ ban đầu
            }
        });
    }

